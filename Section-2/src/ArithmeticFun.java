public class ArithmeticFun {
    public static void main(String args[]) {
        int a,b;
        a=10;
        b=15;
        int result=a+b;
        int diff=b-a;
        int product=a*b;
        int div=b/a;
        System.out.println(result);
        System.out.println(diff);
        System.out.println(product);
        System.out.println(div);

        result+=20;
        System.out.println(result);

        result++;
        System.out.println(result);
    }
}
